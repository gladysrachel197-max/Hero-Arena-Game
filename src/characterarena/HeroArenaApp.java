    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
  if (manager.getHeroes().size() < 2) {
    javax.swing.JOptionPane.showMessageDialog(this,
            "You need at least 2 heroes to battle.");
    return;   
}

String[] names = new String[manager.getHeroes().size()];
for (int i = 0; i < manager.getHeroes().size(); i++) {
    names[i] = manager.getHeroes().get(i).getName();
}

String hero1Name = (String) javax.swing.JOptionPane.showInputDialog(
        this,
        "Select first hero:",
        "Battle",
        javax.swing.JOptionPane.QUESTION_MESSAGE,
        null,
        names,
        names[0]);

if (hero1Name == null) return;

String hero2Name = (String) javax.swing.JOptionPane.showInputDialog(
        this,
        "Select second hero:",
        "Battle",
        javax.swing.JOptionPane.QUESTION_MESSAGE,
        null,
        names,
        names[0]);

if (hero2Name == null || hero1Name.equals(hero2Name)) {
    javax.swing.JOptionPane.showMessageDialog(this,
            "Choose two different heroes.");
    return;
}

Hero hero1 = null;
Hero hero2 = null;

for (Hero h : manager.getHeroes()) {
    if (h.getName().equals(hero1Name)) hero1 = h;
    if (h.getName().equals(hero2Name)) hero2 = h;
}

int score1 = hero1.getAttack() + hero1.getDefence() + hero1.getHp();
int score2 = hero2.getAttack() + hero2.getDefence() + hero2.getHp();

String result;
if (score1 > score2) {
    result = hero1.getName() + " WINS! 🏆";
    hero1.addExperience(10);
} else if (score2 > score1) {
    result = hero2.getName() + " WINS! 🏆";
    hero2.addExperience(10);
} else {
    result = "It's a DRAW 🤝";
}


javax.swing.JOptionPane.showMessageDialog(this,
        hero1.getName() + " vs " + hero2.getName()
        + "\n\n" + result);
  // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

