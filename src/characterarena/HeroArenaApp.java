    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String name = javax.swing.JOptionPane.showInputDialog (this, "Enter hero name:");
if (name == null || name.isEmpty()) return;


String[] types = {"Warrior", "Mage", "Archer"};
String type = (String) javax.swing.JOptionPane.showInputDialog(
        this,
        "Choose hero class:",
        "Hero Class",
        javax.swing.JOptionPane.QUESTION_MESSAGE,
        null,
        types,
        types[0]);

if (type == null) return;

Hero hero;
String hpInput = javax.swing.JOptionPane.showInputDialog(this, "Enter HP:");
 if (hpInput == null) return;

String attackInput = javax.swing.JOptionPane.showInputDialog(this, "Enter Attack:");
if (attackInput == null) return;

String defenceInput = javax.swing.JOptionPane.showInputDialog(this, "Enter Defence:");
if (defenceInput == null) return;

int hp;
int attack;
int defence;

try {
    hp = Integer.parseInt(hpInput);
    attack = Integer.parseInt(attackInput);
  defence = Integer.parseInt(defenceInput);
}
 catch (NumberFormatException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please enter valid numbers!");
    return;
}




if (type.equals("Warrior")) {
    hero = new Warrior(name, hp, attack, defence);
} 
else if (type.equals("Mage")) {
    hero = new Mage(name, hp, attack, defence);
} 
else {
    hero = new Archer(name, hp, attack, defence);
}






manager.addHero(hero);
javax.swing.JOptionPane.showMessageDialog(this, "Hero created!");
   // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

